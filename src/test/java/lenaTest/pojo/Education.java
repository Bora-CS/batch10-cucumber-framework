package lenaTest.pojo;

public class Education {
	public String school;
	public String degree;
	public String major;
	public String from;
	public String to;
	public String Description;

	public Education(String school, String degree, String major, String from, String to, String Description) {

		this.school = school;
		this.degree = degree;
		this.major = major;
		this.from = from;
		this.to = to;
		this.Description = Description;
	}

}
