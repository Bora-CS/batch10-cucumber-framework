package ardal_practice.pojo;

public class BoraTechUserProfile {

	public String skills;
	public String _id;
	public String user;
	public String __v;
	public String bio;
	public String company;
	public String location;
	public String status;
	public String githubusername;
	public String website;
	public String date;
	public String education;
	public String experience;

	public BoraTechUserProfile(String skills, String _id, String user, String __v, String bio, String company,
			String location, String status, String githubusername, String website, String date, String education,
			String experience) {

		this.skills = skills;
		this._id = _id;
		this.user = user;
		this.__v = __v;
		this.bio = bio;
		this.company = company;
		this.location = location;
		this.status = status;
		this.githubusername = githubusername;
		this.website = website;
		this.date = date;
		this.education = education;
		this.experience = experience;

	}

}
